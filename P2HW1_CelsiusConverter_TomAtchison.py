# input temperature in degrees Celsius.
C = float (input ( "Enter temperature in degrees Celsius: " ) )
# convert from Celsius to Fahrenheit.
F = ( ( 9 / 5 ) * C ) + 32
# output temperature in degrees Fahrenheit.
print ( "The temperature in degrees Fahrenheit is: ")
print ( F )


